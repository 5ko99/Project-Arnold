package com.example.aoc.project_arnold.Objects;

public class Record {
    String exercises;
    int  imgs;
    String reps;
    String kg;
    String date;

    public void setExercises(String exercises) {
        this.exercises = exercises;
    }

    public void setImgs(int imgs) {
        this.imgs = imgs;
    }

    public void setReps(String reps) {
        this.reps = reps;
    }

    public void setKg(String kg) {
        this.kg = kg;
    }

    public void setDate(String date) {
        this.date = date;
    }


    public String getExercises() {
        return exercises;
    }

    public int getImgs() {
        return imgs;
    }

    public String getReps() {
        return reps;
    }

    public String getKg() {
        return kg;
    }

    public String getDate() {
        return date;
    }


}
